void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int tpm;

	i = 0;
	while (tab[i] && i < size / 2)
	{
		tpm = tab[i];
		tab[i] = tab[size - 1 - i];
		tab[size - 1 - i] = tpm;
		i++;
	}
}
